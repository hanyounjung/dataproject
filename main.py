def render_card(container, item):
    with container:
        st.markdown(f"### {item['icon']} {item['career']}")
        st.markdown("**어떤 전공이 어울려?**")
        st.markdown(" · " + " · ".join(item["majors"]))
        st.markdown("**이런 성격이면 찰떡!**")
        st.markdown(f"{item['fit']}")
        st.divider()

